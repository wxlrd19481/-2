// функция для проверки - является ли число простым
function P(n) {
  // 0, 1 и отрицательные числа не являются простыми
  if (n <= 1) {
    return false
  }
  
  // проверяем делится ли число на любое число от 2 до его корня
  for (let i = 2; i <= Math.sqrt(n); i++) {
    if (n % i === 0) {
      return false;
    }
  }
  
  return true;
}

// выводим простые числа от 1 до своего порядкового номера
let myNumber = 10; // замените на свой порядковый номер
console.log("Простые числа от 1 до", myNumber, ":");
for (let i = 1; i <= myNumber; i++) {
  if (P(i)) {
    console.log(i);
  }
}
